package epamcom.calculator;

public class Operations {
	    double addition(double n, double m) {
			return n + m;
		}
		double substraction(double n, double m) {
			return n - m;
		}
		double multiplication(double n, double m) {
			return n * m;
		}
		double division(double n, double m) {
			return n / m;
		}
}